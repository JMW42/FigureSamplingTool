def preprocess_command_line(line):
    return line.split(' ')



def command_loop():
    
    inp:str = input('<')
    cmd:list = preprocess_command_line(inp)
    
    print(cmd)

    if len(cmd) >= 1 and cmd[0] in COMMANDS:
        COMMANDS[cmd[0]](cmd[1:])



def command_help(args):
    print(f'help: {args}')
    for cmd in COMMANDS:
        print(f' - {cmd}')

# ##################################################################################################################################
# ##################################################################################################################################

COMMANDS = {'help':command_help}

